function [text] = gen_sim_summary(disp_name,X0,X1,Nav,dp_av)
text = {disp_name,...
        sprintf('Total Number Density (m^3):  %.2e',X0),...
        sprintf('Volume Fraction (ppm):  %.3f',X1*10^6),...
        sprintf('Nav:  %.1f',Nav),...
        sprintf('dp average (nm): %.1f',dp_av*10^9)};
    
text = sprintf('%s\n',text{:});
end

